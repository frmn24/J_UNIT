package com.example.junit;

public class SimpleCalculator {
    /**
     * Menjumlahkan dua bilangan bulat.
     *
     * @param numberA Bilangan bulat pertama yang akan dijumlahkan.
     * @param numberB Bilangan bulat kedua yang akan dijumlahkan.
     * @return Hasil penjumlahan dari numberA dan numberB.
     */
    public static int add(int numberA, int numberB){
        // Menggunakan operator + untuk menjumlahkan numberA dan numberB.
        return numberA + numberB;
    }
}

